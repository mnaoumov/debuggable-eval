import type { BuildOptions } from 'esbuild';

import { context } from 'esbuild';

async function main(): Promise<void> {
  const commonOptions: BuildOptions = {
    banner: {
      js: '/* THIS IS A GENERATED/BUNDLED FILE BY ESBUILD */'
    },
    bundle: true,
    entryPoints: ['src/index.ts'],
    logLevel: 'info',
    platform: 'node',
    sourcemap: 'inline',
    target: 'ES2024',
    treeShaking: true
  };

  const [cjsContext, esmContext] = await Promise.all([
    context({
      ...commonOptions,
      format: 'cjs',
      outdir: 'dist/lib/cjs',
      outExtension: { '.js': '.cjs' }
    }),
    context({
      ...commonOptions,
      format: 'esm',
      outdir: 'dist/lib/esm',
      outExtension: { '.js': '.mjs' }
    })
  ]);

  await Promise.all([
    cjsContext.rebuild(),
    esmContext.rebuild()
  ]);

  await Promise.all([
    cjsContext.dispose(),
    esmContext.dispose()
  ]);
}

await main();
